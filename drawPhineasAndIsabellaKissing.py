def oval(r):
    rt(45)
    for loop in range(2):
        circle(r, 90)
        circle(0.8 * r, 90)


def calculateAngle(tup1, tup2):
    dy = tup2[1] - tup1[1]
    dx = tup2[0] - tup1[0]
    angle = atan(abs(dy / dx))
    return angle / pi * 180


