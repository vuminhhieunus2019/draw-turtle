def oval(r):
    for loop in range(2):
        circle(r, 90)
        circle(r/2, 90)


def calculateAngle(tup1, tup2):
    dy = tup2[1] - tup1[1]
    dx = tup2[0] - tup1[0]
    angle = atan(abs(dy / dx))
    return angle / pi * 180


